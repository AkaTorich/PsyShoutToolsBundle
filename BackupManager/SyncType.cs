namespace BackupManager.Models
{
    public enum SyncType
    {
        Full,            // Полная синхронизация (копирование + удаление устаревших файлов в backup)
        Incremental,     // Инкрементальное копирование (только новые и измененные файлы, без удаления)
        Decremental,     // Зеркальная синхронизация (полная синхронизация как Full)
        TwoWay           // Односторонняя синхронизация (аналогично Full, но с другим названием)
    }
}